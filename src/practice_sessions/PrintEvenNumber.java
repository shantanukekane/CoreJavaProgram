package practice_sessions;

import java.util.Scanner;

public class PrintEvenNumber {

	public static void main(String[] args) {
		
		int n;
		
		System.out.print("Enter any Number: ");
		Scanner s = new Scanner(System.in);
		
		n = s.nextInt();
		
		for(int i = 0;i<=n;i+=2)
		{
			System.out.print(i+" ");
		}
		
		
		
		
		
		
		
		
		
		
		
		

	}

}

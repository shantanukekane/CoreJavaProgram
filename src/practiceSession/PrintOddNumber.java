package practiceSession;

import java.util.Scanner;

public class PrintOddNumber {

	public static void main(String[] args) {
		
		int num;
		System.out.print("Enter any Number: ");
		Scanner s = new Scanner(System.in);
		
		num = s.nextInt();
		
		for(int i = 1;i<num;i+=2)
		{
			System.out.print(i+" ");;
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}

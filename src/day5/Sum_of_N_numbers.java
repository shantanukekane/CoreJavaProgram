package day5;

import java.util.Scanner;

public class Sum_of_N_numbers {

	public static void main(String[] args) {
		
		
		int num,sum = 0;
		
		System.out.println("Enter any Numbers: ");
		Scanner s = new Scanner(System.in);
		
		num = s.nextInt();
		
		for(int i = 1;i<=num;i++) // 1 
		{
			sum = sum + i;  // 1 2 
		}
		System.out.print(sum);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}

package day5;

import java.util.Scanner;

public class No_positive_Negative {

	public static void main(String[] args) {
		
		
		int num;
		
		System.out.print("Enter any Value: ");
		Scanner s = new Scanner(System.in);
		
		num = s.nextInt();
		
		if(num<0)
		{
			System.out.print("Negative Number: "+num);
		}
		else if(num>0)
		{
			System.out.print("Positive Number: "+num);
		}
		else
		{
			System.out.print("Neither positive nor Negative Number: "+num);
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
